/*******************************************************************************
  Function:
    void SYS_Initialize ( void *data )

  Summary:
    Initializes the board, services, drivers, application and other modules.

  Remarks:
 */

void SYS_Initialize ( void* data )
{

    NVMCTRL_Initialize( );

  
    PORT_Initialize();

    CLOCK_Initialize();




    SERCOM2_USART_Initialize();

    EVSYS_Initialize();

    EIC_Initialize();

    PDEC_QDECInitialize();

    TCC0_PWMInitialize();

    ADC0_Initialize();
    ADC1_Initialize();



    X2CScope_Init();
    PMSM_FOC_Initialize();


    NVIC_Initialize();

}


